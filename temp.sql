CREATE OR REPLACE PROCEDURE pr_get_marke_id(
    p_bezeichnung IN  VARCHAR2,
    p_marke_id    OUT INTEGER
)
AS
BEGIN
    SELECT pk_marke_id
    INTO   p_marke_id
    FROM   marke
    WHERE  bezeichnung = p_bezeichnung;
END;
/
SHOW ERRORS;

CREATE OR REPLACE PROCEDURE pr_create_fahrzeug(
    p_typ         IN VARCHAR2,
    p_marke       IN VARCHAR2,
    p_modell      IN VARCHAR2,
    p_preis       IN NUMBER,
    p_klasse      IN CHAR
)
AS
    v_marke_id     INTEGER;
    v_fahrzeug_id  INTEGER;
BEGIN
    pr_get_marke_id(p_marke, v_marke_id);

    INSERT INTO fahrzeug (fk_marke_id, modell, preis, fk_klasse)
    VALUES (v_marke_id, p_modell, p_preis, p_klasse)
    RETURNING pk_fahrzeug_id INTO v_fahrzeug_id;

    IF p_typ = 'PKW' THEN
        INSERT INTO pkw (pk_fk_fahrzeug_id, hatklima, anzahltueren)
        VALUES (v_fahrzeug_id, 0, 4);
    ELSIF p_typ = 'LKW' THEN
        INSERT INTO lkw (pk_fk_fahrzeug_id, gewicht, sitze)
        VALUES (v_fahrzeug_id, 3500, 2);
    END IF;
END;
/
SHOW ERRORS;

DECLARE
    CURSOR c_kf IS
        SELECT pk_fk_kunde_id, pk_fk_fahrzeug_id, datumbis
        FROM   kunde_fahrzeug
        FOR UPDATE OF datumbis;

    v_row c_kf%ROWTYPE;
BEGIN
    OPEN c_kf;
    LOOP
        FETCH c_kf INTO v_row;
        EXIT WHEN c_kf%NOTFOUND;

        IF v_row.datumbis IS NOT NULL AND v_row.datumbis > SYSDATE THEN
            UPDATE kunde_fahrzeug
            SET    datumbis = NULL
            WHERE  CURRENT OF c_kf;
        END IF;
    END LOOP;
    CLOSE c_kf;
END;
/
SHOW ERRORS;

CREATE OR REPLACE TRIGGER tr_kf_after_update_datumbis
AFTER UPDATE OF datumbis ON kunde_fahrzeug
FOR EACH ROW
WHEN (NEW.datumbis IS NOT NULL AND OLD.datumbis IS NULL)
DECLARE
    CURSOR c_fzg IS
        SELECT f.modell, m.bezeichnung
        FROM   fahrzeug f
               JOIN marke m ON m.pk_marke_id = f.fk_marke_id
        WHERE  f.pk_fahrzeug_id = :new.pk_fk_fahrzeug_id;

    v_modell fahrzeug.modell%TYPE;
    v_marke  marke.bezeichnung%TYPE;
BEGIN
    OPEN c_fzg;
    LOOP
        FETCH c_fzg INTO v_modell, v_marke;
        EXIT WHEN c_fzg%NOTFOUND;

        DBMS_OUTPUT.PUT_LINE('Das Fahrzeug ' || v_modell || ' (' || v_marke || ') wurde zurückgebracht');
    END LOOP;
    CLOSE c_fzg;
END;
/
SHOW ERRORS;

CREATE OR REPLACE TRIGGER tr_kf_before_insert_check_fs
BEFORE INSERT ON kunde_fahrzeug
FOR EACH ROW
DECLARE
    CURSOR c_check IS
        SELECT 1
        FROM   kunde_fuehrerschein kfs
               JOIN fahrzeug f ON f.pk_fahrzeug_id = :new.pk_fk_fahrzeug_id
        WHERE  kfs.pk_fk_kunde_id = :new.pk_fk_kunde_id
        AND    kfs.pk_fk_klasse = f.fk_klasse;

    v_dummy NUMBER;
BEGIN
    OPEN c_check;
    LOOP
        FETCH c_check INTO v_dummy;
        EXIT WHEN c_check%FOUND OR c_check%NOTFOUND;
    END LOOP;
    CLOSE c_check;

    IF v_dummy IS NULL THEN
        RAISE_APPLICATION_ERROR(-20001, 'Der Kunde besitzt keine Fahrerlaubnis für das betreffende Fahrzeug.');
    END IF;
END;
/
SHOW ERRORS;
